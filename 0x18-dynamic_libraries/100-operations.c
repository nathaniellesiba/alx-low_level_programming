#include "main.h"

/**
* add - adding two numbers
* Multiply - multiplying nums
* Subtract - sending greetings
* Divide - divide two nums
* @a: the first integer
* @b: the second integer
* Return: always 0
*/


int add(int a, int b)
{
return (a + b);
}


int subtract(int a, int b)
{
return (a - b);
}


int multiply(int a, int b)
{
return (a * b);
}


float divide(int a, int b)
{
return ((float)a / b);
}
